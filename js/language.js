// Function to Change Language
function changeLanguage(language) {
    const elements = Object.keys(translations[language]);
    elements.forEach((element) => {
      if (element === 'image1') {
        const image1 = document.getElementById('image1');
        image1.getElementsByTagName('img')[0].src = translations[language][element];
      } else {
        const translation = translations[language][element];
        document.getElementById(element).textContent = translation;
      }
    });
  }
  
  
  // Function to Store Selected Language
  function setLanguagePreference(language) {
    // Store the selected language in a cookie or local storage
    // For simplicity, let's use local storage here.
    localStorage.setItem('preferredLanguage', language);
  }
  
  // Function to Get Selected Language
  function getLanguagePreference() {
    // Retrieve the selected language from the cookie or local storage
    // For simplicity, let's use local storage here.
    return localStorage.getItem('preferredLanguage');
  }
  
  // Function to Trigger Language Change
  function onLanguageChange(event) {
    const selectedLanguage = event.target.id === 'chineseLink' ? 'chinese' : 'english';
    changeLanguage(selectedLanguage);
    setLanguagePreference(selectedLanguage);
  }

  // Trigger Language Change on Page Load
  document.addEventListener('DOMContentLoaded', () => {
    const preferredLanguage = getLanguagePreference();
    const languageToApply = preferredLanguage || 'chinese'; // Set default language here
    changeLanguage(languageToApply);
  });
  
  // Add Click Event Listeners to Language Links
  const chineseLink = document.getElementById('chineseLink');
  const englishLink = document.getElementById('englishLink');
  chineseLink.addEventListener('click', onLanguageChange);
  englishLink.addEventListener('click', onLanguageChange);

  
 
